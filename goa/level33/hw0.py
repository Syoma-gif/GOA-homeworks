def word_len(sentance):  # განსაზღვრავს ფუნქციას სახელად word_len, რომელიც იღებს წინადადებას
    result = []          # ცარიელი სია, სადაც შეინახება სიტყვები + მათი სიგრძე
    words = ""           # ცარიელი სტრიქონი, დროებითი სიტყვების შესაგროვებლად

    for i in sentance:   # გადავლა თითოეულ სიმბოლოზე წინადადებაში
        if i != " ":     # თუ სიმბოლო არ არის space
            words += i   # დაამატე სიმბოლო მიმდინარე სიტყვაში
        else:            # თუ space-ს გადააწყდა
            result.append(words + str(len(words)))  # დაამატე სიტყვა და მისი სიგრძე სიაში
            words = ""   # გაასუფთავე სიტყვა შემდეგი სიტყვის დასაწყებად

    result.append(words + str(len(words)))  # დაამატე ბოლო სიტყვა და მისი სიგრძე
    print(result)       # დაბეჭდე საბოლოო შედეგი

word_len("Hello how are you")  # ფუნქციის გამოძახება კონკრეტული ტექსტით
